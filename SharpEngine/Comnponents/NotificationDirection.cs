namespace SharpEngine.Comnponents;

public enum NotificationDirection
{
    /// <summary>
    /// The notification will slide in from the left.
    /// </summary>
    Left    = 0x1F,

    /// <summary>
    /// The notification will slide in from the right.
    /// </summary>
    Right   = 0x2F,

    /// <summary>
    /// The notification will slide in from the top.
    /// </summary>
    Top     = 0x3F,

    /// <summary>
    /// The notification will slide in from the bottom.
    /// </summary>
    Down    = 0x4F,
}
